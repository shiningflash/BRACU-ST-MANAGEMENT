package com.example.bracu_st_management;

public class Faculty {
    private String Email_id, Password, Name, Contact_no, Department, Faculty_initial;

    public String getEmail_id() {
        return Email_id;
    }

    public void setEmail_id(String email_id) {
        Email_id = email_id;
    }

    public String getPassword() {
        return Password;
    }

    public void setPassword(String password) {
        Password = password;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getContact_no() {
        return Contact_no;
    }

    public void setContact_no(String contact_no) {
        Contact_no = contact_no;
    }

    public String getDepartment() {
        return Department;
    }

    public void setDepartment(String department) {
        Department = department;
    }

    public String getFaculty_initial() {
        return Faculty_initial;
    }

    public void setFaculty_initial(String faculty_initial) {
        Faculty_initial = faculty_initial;
    }
}
